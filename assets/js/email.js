/* ===========================================================
 * Email — uses EmailJS browser SDK to dispatch report emails.
 * Template assumed to accept: to_email, subject, message, from_name.
 * =========================================================== */

(function (global) {
  'use strict';

  function initEmailJS() {
    const cfg = global.SERPSCOPE.app.getSettings();
    if (!cfg.ejsKey) return false;
    if (global.emailjs?.init) {
      try { global.emailjs.init({ publicKey: cfg.ejsKey }); return true; } catch { return false; }
    }
    return false;
  }

  function buildReportEmail(target, comps, actions, opts = {}) {
    const company = global.SERPSCOPE.app.getSettings().company || 'SERPSCOPE';
    const rank = global.SERPSCOPE.renderer.rankOf(target, comps);
    const crit = actions.filter((a) => a.priority === 'P0').length;
    const high = actions.filter((a) => a.priority === 'P1').length;

    const subject = `SEO Audit — ${target.host} — Score ${target.composite} (Grade ${target.grade})`;
    const lines = [];
    lines.push(`SEO INTELLIGENCE REPORT`);
    lines.push(`Prepared by ${company} · ${new Date(target.timestamp).toLocaleString()}`);
    lines.push(`─────────────────────────────────────────`);
    lines.push('');
    lines.push(`TARGET     : ${target.url}`);
    lines.push(`SCORE      : ${target.composite}/100  (Grade ${target.grade})`);
    lines.push(`RANK       : #${rank.position} of ${rank.total} (you + ${comps.length} competitors)`);
    lines.push('');
    lines.push('CATEGORY BREAKDOWN');
    lines.push(`  On-Page    ${pad(target.categories.onpage.score)}   weight 30%`);
    lines.push(`  Technical  ${pad(target.categories.technical.score)}   weight 30%`);
    lines.push(`  Content    ${pad(target.categories.content.score)}   weight 25%`);
    lines.push(`  Off-Page   ${pad(target.categories.offpage.score)}   weight 15%`);
    lines.push('');
    lines.push('COMPETITIVE SET');
    [target, ...comps].sort((a, b) => b.composite - a.composite).forEach((s, i) => {
      const self = s.host === target.host ? '*' : ' ';
      lines.push(`  ${self} #${i + 1}  ${pad(s.composite)}  ${s.host}`);
    });
    lines.push('');
    lines.push(`ACTION SUMMARY`);
    lines.push(`  Critical (P0): ${crit}`);
    lines.push(`  High     (P1): ${high}`);
    lines.push(`  Quick wins   : ${actions.filter((a) => a.quickwin).length}`);
    lines.push('');
    lines.push('TOP PRIORITIES');
    actions.slice(0, 10).forEach((a, i) => {
      lines.push(`  ${i + 1}. [${a.priority}] ${a.title}`);
      lines.push(`     ${a.fix}`);
    });
    lines.push('');
    if (opts.attachJson) {
      lines.push('─────────────────────────────────────────');
      lines.push('RAW AUDIT DATA');
      lines.push(JSON.stringify({
        target: { host: target.host, score: target.composite, grade: target.grade,
                  categories: Object.fromEntries(Object.entries(target.categories).map(([k, v]) => [k, v.score])) },
        competitors: comps.map((c) => ({ host: c.host, score: c.composite, grade: c.grade })),
        actionsCount: actions.length,
      }, null, 2));
    }
    lines.push('');
    lines.push(`— Generated by SERPSCOPE on behalf of ${company}`);
    return { subject, message: lines.join('\n') };
  }

  function pad(n) { return String(n).padStart(3, ' '); }

  async function sendEmail({ to, subject, message }) {
    const cfg = global.SERPSCOPE.app.getSettings();
    if (!cfg.ejsKey || !cfg.ejsService || !cfg.ejsTpl) {
      throw new Error('EmailJS not configured. Open Settings to add your keys.');
    }
    if (!initEmailJS()) throw new Error('EmailJS init failed.');
    const params = {
      to_email: to,
      subject,
      message,
      from_name: cfg.ejsFrom || cfg.company || 'SERPSCOPE',
    };
    const r = await global.emailjs.send(cfg.ejsService, cfg.ejsTpl, params);
    return r;
  }

  global.SERPSCOPE = global.SERPSCOPE || {};
  global.SERPSCOPE.email = { buildReportEmail, sendEmail };
})(window);
